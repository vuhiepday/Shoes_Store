    public void hienThiDL()
    {
        KhachHangDAO khdao = new KhachHangDAO();
        listkh = khdao.readKhachHang();
        tblModel =(DefaultTableModel) tblKhachhang.getModel();
        tblModel.setRowCount(0);
        for(Khachhang1 kh :listkh){
            Object[] row = new Object[]{kh.getMakh(), kh.getTenKH(), kh.getSoDT(), kh.getSoTM()};
        tblModel.addRow(row);
        }
        tblKhachhang.setModel(tblModel);
    }
    public boolean containsDigit(String text) {
        for (char c : text.toCharArray()) {
            if (Character.isDigit(c)) {
                return true;
            }
        }
        return false;
    }
    
