    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed

        KhachHangDAO khdao = new KhachHangDAO();
        String maKH = txtMKH.getText();
        String tenKH = txtTKH.getText();
        String soDTString = txtSDT.getText();
        String soTMString = txtSTM.getText();
        if (containsDigit(tenKH)) {
            JOptionPane.showMessageDialog(this, "Tên khách hàng không được chứa ký tự số.");
        } else if (!soDTString.matches("0\\d{9}")) {
            JOptionPane.showMessageDialog(this, "Số điện thoại phải bắt đầu bằng số 0.");
        } else if (!soDTString.matches("\\d{10}")) {
            JOptionPane.showMessageDialog(this, "Số điện thoại phải là một chuỗi có đúng 10 chữ số.");
        }else if (!soTMString.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Số tiền mua phải là một số nguyên dương.");
        } else {
          String soDT=txtSDT.getText();
            int soTM = Integer.parseInt(soTMString);
            Khachhang1 kh = new Khachhang1();
            kh.setMakh(maKH);
            kh.setTenKH(tenKH);
            kh.setSoDT(soDT);
            kh.setSoTM(soTM);
            int x = khdao.UpdateKH(kh);
            if (x > 0) {
                JOptionPane.showMessageDialog(this, "Sửa thành công");
                hienThiDL();
            } else {
                JOptionPane.showMessageDialog(this, "Sửa thất bại");
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

