    private void btnXuatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXuatActionPerformed
        // TODO add your handling code here:
        try {
        if (listkh.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không có dữ liệu để xuất.");
            return;
        }

        XSSFWorkbook workbook = new XSSFWorkbook();
        XSSFSheet sheet = workbook.createSheet("danhsach");
        XSSFRow row = null;
        Cell cell = null;

        row = sheet.createRow(2);
        cell = row.createCell(0, CellType.STRING);
        cell.setCellValue("DANH SÁCH Khách Hàng");

        row = sheet.createRow(3);
        cell = row.createCell(0, CellType.STRING);
        cell.setCellValue("");

        cell = row.createCell(1, CellType.STRING);
        cell.setCellValue("Mã Khách Hàng");

        cell = row.createCell(2, CellType.STRING);
        cell.setCellValue("Tên Khách Hàng");

        cell = row.createCell(3, CellType.STRING);
        cell.setCellValue("Số Điện Thoại");

        cell = row.createCell(4, CellType.STRING);
        cell.setCellValue("Số Tiền Mua");

        for (int i = 0; i < listkh.size(); i++) {
            Khachhang1 kh = listkh.get(i);
            row = sheet.createRow(4 + i);

            cell = row.createCell(1, CellType.STRING);
            cell.setCellValue(kh.getMakh());

            cell = row.createCell(2, CellType.STRING);
            cell.setCellValue(kh.getTenKH());

            cell = row.createCell(3, CellType.STRING);
            cell.setCellValue(kh.getSoDT());

            cell = row.createCell(4, CellType.STRING);
            cell.setCellValue(kh.getSoTM());
        }

        File f = new File("D://danhsach.xlsx");
        try (FileOutputStream fis = new FileOutputStream(f)) {
            workbook.write(fis);
        } catch (FileNotFoundException ex) {
            ex.printStackTrace();
        } catch (IOException ex) {
            ex.printStackTrace();
        }

        JOptionPane.showMessageDialog(this, "Xuất thành công vào D:\\danhsach.xlsx");

    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Lỗi khi xuất file.");
    }
    }//GEN-LAST:event_btnXuatActionPerformed

