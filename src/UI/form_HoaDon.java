    private void showDataInTable() {
        HoaDonDAO hoadon = new HoaDonDAO();
        List<HoaDon> list = hoadon.readAllHoaDon();
        fillToTable(list);
    }

    private void fillToTable(List<HoaDon> list) {
        DefaultTableModel model = (DefaultTableModel) table_hoadon.getModel();
        model.setRowCount(0);
        for(HoaDon nv : list){
            Object[] row = new Object[]{nv.getIdHoaDon(), nv.getNhanVien(), nv.getKhachHang(), nv.getNgayLap(), nv.getTongTien()};
            model.addRow(row);
        }
    }
