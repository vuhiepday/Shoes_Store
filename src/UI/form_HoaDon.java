    private void btn_chitietActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_chitietActionPerformed
        int selectedRow = table_hoadon.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tblHangHoa.getModel();
        model.setRowCount(0);
        if (selectedRow >= 0) {
            int idHoaDon = (int) table_hoadon.getValueAt(selectedRow, 0);
            HoaDonDAO hoaDonDAO = new HoaDonDAO();
            List<ChiTietHoaDon> chiTietList = hoaDonDAO.getChiTietHoaDon(idHoaDon);
            if (!chiTietList.isEmpty()) {
                for (ChiTietHoaDon chiTiet : chiTietList) {
                    Object[] row = new Object[]{chiTiet.getTenSanPham(), chiTiet.getSoLuong(), chiTiet.getThanhTien()};
                    model.addRow(row);
                }
                int a = (int) table_hoadon.getValueAt(selectedRow, 4);
                tongtien.setText(String.valueOf(a));
                String b = (String) table_hoadon.getValueAt(selectedRow, 2);
                tenkhach.setText(b);
            } else {
                JOptionPane.showMessageDialog(this, "Không có thông tin chi tiết cho hóa đơn này.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một hàng để xem chi tiết.");
        }
    }//GEN-LAST:event_btn_chitietActionPerformed

