    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        int selectedRow = table_hoadon.getSelectedRow();
        if (selectedRow >= 0) {
            int idHoaDon = (int) table_hoadon.getValueAt(selectedRow, 0);
            HoaDonDAO dao = new HoaDonDAO();
            boolean success = dao.deleteHoaDon(idHoaDon);
            if (success) {
                JOptionPane.showMessageDialog(this, "Hóa đơn đã được xóa thành công.");
                showDataInTable();
            } else {
                JOptionPane.showMessageDialog(this, "Xóa hóa đơn thất bại.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một hàng để xóa.");
        }
    }//GEN-LAST:event_btn_xoaActionPerformed

