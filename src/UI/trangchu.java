    private void addToHangHoaTable(String tenSP, String soLuong, int DonGia) {
        DefaultTableModel model = (DefaultTableModel) tblHangHoa.getModel();
        int soLuongInt = Integer.parseInt(soLuong);
        int totalPrice = DonGia * soLuongInt;
        // Kiểm tra xem sản phẩm đã tồn tại trong bảng chưa
        boolean daTonTai = false;
        for (int i = 0; i < model.getRowCount(); i++) {
            if (model.getValueAt(i, 0).equals(tenSP)) {
                // Nếu đã tồn tại, cập nhật số lượng
                int soLuongHienTai = Integer.parseInt(model.getValueAt(i, 1).toString());
                int tongSoLuongMoi = soLuongHienTai + soLuongInt;
                model.setValueAt(tongSoLuongMoi, i, 1); // Cập nhật số lượng mới
                daTonTai = true;
                break;
            }
        }
        if (!daTonTai) {
            // Nếu chưa tồn tại, thêm mới vào bảng
            model.addRow(new Object[]{tenSP, soLuong, totalPrice});
        }
        updateTotalPrice();
        
    }

    private void updateTotalPrice() {
        //Hiển thị giá trị tổng tiền
        KhachHangDAO khachHangDAO = new KhachHangDAO();
        DefaultTableModel model = (DefaultTableModel) tblHangHoa.getModel();
        int rowCount = model.getRowCount();
        int totalPrice = 0;
        for (int i = 0; i < rowCount; i++) {
            int totalPriceForRow = (int) model.getValueAt(i, 2); // Lấy giá trị tổng tiền từ cột thứ 3 (index 2)
            totalPrice += totalPriceForRow;
        }
        txtTongTien.setText(String.valueOf(totalPrice));
        String tenKH = txt_TenKhachHang.getText();
        String sDT = txt_SDTKH.getText();
        int idKH = khachHangDAO.kiemTraVaLayId(tenKH, sDT);
        int tien = totalPrice;
        txt_ThanhTien.setText(String.valueOf(khachHangDAO.Lay_Phan_Tram_TongTien_KH(idKH,tien)));
        
    }

