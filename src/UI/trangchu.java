   private void fillToTable(List<SanPham_TrangChu> list) {
        DefaultTableModel model = (DefaultTableModel) table_Sp_trangchu.getModel();
        model.setRowCount(0);
        for(SanPham_TrangChu sp : list){
            model.addRow(new Object[]{sp.getTenSanPham(), sp.getKichThuoc(), sp.getDonGia(),sp.getTonCuoi(),sp.getHinhAnh() });
        }
    }
    private void showSelectedRow() {
        int selectedRow = table_Sp_trangchu.getSelectedRow();
        if (selectedRow >= 0) {
            String imagePath = table_Sp_trangchu.getValueAt(selectedRow, 4).toString();
            displayImage(imagePath);
        }
    }
    private void displayImage(String imagePath) {
    File imageFile = new File(imagePath);
    if (imageFile.exists()) {
        ImageIcon icon = new ImageIcon(imagePath);
        Image img = icon.getImage();

        // Get the original dimensions of the image
        int originalWidth = icon.getIconWidth();
        int originalHeight = icon.getIconHeight();

        // Get the dimensions of the component
        int componentWidth = txt_Anh.getWidth();
        int componentHeight = txt_Anh.getHeight();

        // Calculate the scaling factor to maintain the aspect ratio
        double widthScale = (double) componentWidth / originalWidth;
        double heightScale = (double) componentHeight / originalHeight;
        double scale = Math.min(widthScale, heightScale);

        // Calculate the new dimensions
        int newWidth = (int) (originalWidth * scale);
        int newHeight = (int) (originalHeight * scale);

        // Scale the image to the new dimensions
        Image scaledImg = img.getScaledInstance(newWidth, newHeight, Image.SCALE_SMOOTH);
        txt_Anh.setIcon(new ImageIcon(scaledImg));
    } else {
        JOptionPane.showMessageDialog(null, "File không tồn tại: " + imagePath);
        txt_Anh.setIcon(null);
    }
    txt_Anh.repaint();
}

