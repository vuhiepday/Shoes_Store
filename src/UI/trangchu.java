    private void btn_ThemSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemSPActionPerformed
         // Lấy dữ liệu từ các trường nhập liệu
        String tenSP = txtTenSP_trangchu.getText();
        String soLuong = txtSoLuong_trangchu.getText();
        String donGiaStr = txtDonGia.getText();

        // Kiểm tra xem trường có trống không
        if (soLuong.isEmpty() || donGiaStr.isEmpty()|| tenSP.isEmpty()) {
            // Nếu trống, hiển thị thông báo
            JOptionPane.showMessageDialog(this, "Vui lòng đủ thông tin.", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else {

            // Nếu không trống, chuyển đổi đơn giá thành kiểu số
            int donGia = Integer.parseInt(donGiaStr);
            // Thêm dữ liệu vào bảng
            addToHangHoaTable(tenSP, soLuong, donGia);
            // Xóa nội dung trong các trường nhập liệu
            txtSoLuong_trangchu.setText("");
            txtTenSP_trangchu.setText("");
            txtDonGia.setText("");
        }
     
    }//GEN-LAST:event_btn_ThemSPActionPerformed

